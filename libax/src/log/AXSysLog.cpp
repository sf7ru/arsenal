AXSysLog::AXSysLog(void)
{
    source = nil;
}
// ***************************************************************************
// FUNCTION
//      AXSysLog::~AXSysLog
// PURPOSE
//
// PARAMETERS
//         void --
// RESULT
//        --
// ***************************************************************************
AXSysLog::~AXSysLog(void)
{
    close();
}
// ***************************************************************************
// FUNCTION
//      AXSysLog::log
// PURPOSE
//
// PARAMETERS
//      AXLOGDL        v_level --
//      const char *   msg     --
// RESULT
//      void  --
// ***************************************************************************
void AXSysLog::log(AXLOGDL      v_level,
                   const char * msg,
                   ...)
{
    va_list         st_va_list;

    ENTER(true);

    va_start(st_va_list, msg);

    log_l(v_level, (PSTR)msg, st_va_list);

    va_end(st_va_list);

    QUIT;
}

