// ***************************************************************************
// FUNCTION
//      la6_sum_calc
// PURPOSE
//
// PARAMETERS
//      PVOID p_data --
//      UINT  u_size --
// RESULT
//      U8    --
// ***************************************************************************
U8 simplesum_calc(PVOID p_data, UINT u_size)
{
    U8        b_sum           = 0;
    UINT        u_left;
    PU8       p_on_data;


    if (p_data && u_size)
    {
        p_on_data   = p_data;
        b_sum       = *(p_on_data++);
        u_left      = u_size;

        while (--u_left)
        {
            b_sum += *(p_on_data++);
        }
    }

    return b_sum;
}
