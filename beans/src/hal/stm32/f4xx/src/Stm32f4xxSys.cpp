#include <stm32f4xx_hal.h>

#include <Sys.h>
#include <axstring.h>

// ---------------------------------------------------------------------------
// -------------------------------- FUNCTIONS --------------------------------
// -----------------|---------------------------(|------------------|---------

INT Sys::getUidString(PSTR           buff,
                      INT            size)
{
    INT         result          = 0;
    U32         bin             [ 3 ];

    ENTER(true);

    if (size >= ((8 * 3) + 1))
    {
        HAL_GetUID((uint32_t *) bin);

        strz_bin2hex(buff +  0, &bin[2], 4);
        strz_bin2hex(buff +  8, &bin[1], 4);
        strz_bin2hex(buff + 16, &bin[0], 4);

        result = true;
    }

    RETURN(result);
}
